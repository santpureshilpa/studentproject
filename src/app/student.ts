export class Student {
    studentId:number;
    firstName:string;
    lastName:string;
    gender:string;
    semster:number;
    mobileNumber:number;

    constructor(studentId:number,firstName:string,lastName:string, gender:string,semester:number,
         mobileNumber:number){
           this.studentId=studentId;
           this.firstName=firstName;
           this.lastName=lastName;
           this.gender=gender;
           this.semster=semester;
           this.mobileNumber=mobileNumber;
 
 

    }
}
          
    

